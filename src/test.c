int test_fft_inc = 0;

void resetTest()
{
    error_count = 0;
    IniWriteString(INI_FILE, "Error_Count", NULL);
    memset(maxerr_recovery_mode, 0, sizeof(maxerr_recovery_mode));
    lasterr_point = 0;
    memset(last_bit, 0, sizeof(last_bit));
    memset(last_maxerr, 0, sizeof(last_maxerr));
    total_error_points = 0;
    clear_timers();
    IniWriteInt(INI_FILE, "FFT_Increment", test_fft_inc);

    res64[0] = 0;
    cert64[0] = 0;

    IniWriteString(INI_FILE, "Gerbicz_Error_Count", NULL);
    ERRCHK = (IniGetInt(INI_FILE, "ErrorCheck", 0) != 0);
    CUMULATIVE_TIMING = IniGetInt(INI_FILE, "CumulativeTiming", 0);
}

int DoTest(
    char *sgk,
    char *sgb,
    unsigned long n,
    int	incr,
    uint64_t refRes64,
    uint64_t refCert64)
{
    int res = FALSE;
    int proof = FALSE;
    if (PROOFMODE == FullTest)
    {
        proof = TRUE;
        PROOFMODE = SavePoints;
    }

    resetTest();
    if (!process_num(ABCVARAS, sgk, sgb, n, incr, 0, &res))
    {
        OutputBoth("Test failed.\n");
        return FALSE;
    }
    if (PROOFMODE == NoProof || PROOFMODE == SavePoints || PROOFMODE == BuildCert)
    {
        if ((refRes64 == 0 && !res) || (refRes64 != 0 && res))
        {
            OutputBoth("Primality mismatch.\n");
            return FALSE;
        }
        if (refRes64 != 0)
        {
            uint64_t testRes64;
            ReadUInt64(res64, &testRes64);
            if (testRes64 != refRes64)
            {
                OutputBoth("Residue mismatch.\n");
                return FALSE;
            }
        }
    }
    if (refRes64 == 0 && cert64[0] == 0 && n < 1000) // APRCL test
    {
        if (proof)
            PROOFMODE = FullTest;
        return TRUE;
    }
    if (PROOFMODE == SavePoints || PROOFMODE == BuildCert)
    {
        cert64[16] = 0;
        uint64_t testCert64;
        ReadUInt64(cert64, &testCert64);
        if (refCert64 == 0)
            refCert64 = testCert64;
        if (testCert64 != refCert64)
        {
            OutputBoth("Raw certificate mismatch.\n");
            return FALSE;
        }
    }

    if (proof)
    {
        res = FALSE;
        PROOFMODE = BuildCert;
        resetTest();
        IniWriteInt(INI_FILE, "FFT_Increment", test_fft_inc + 1);
        if (!process_num(ABCVARAS, sgk, sgb, n, incr, 0, &res))
        {
            OutputBoth("BuildCert failed.\n");
            return FALSE;
        }

        uint64_t testRes64;
        if ((refRes64 == 0 && !res) || (refRes64 != 0 && res))
        {
            OutputBoth("BuildCert primality mismatch.\n");
            return FALSE;
        }
        if (refRes64 != 0)
        {
            ReadUInt64(res64, &testRes64);
            if (testRes64 != refRes64)
            {
                OutputBoth("BuildCert residue mismatch.\n");
                return FALSE;
            }
        }

        cert64[16] = 0;
        uint64_t testCert64;
        ReadUInt64(cert64, &testCert64);
        if (testCert64 != refCert64)
        {
            OutputBoth("BuildCert Raw certificate mismatch.\n");
            return FALSE;
        }
        ReadUInt64(cert64 + 17, &testCert64);

        PROOFMODE = VerifyCert;
        resetTest();
        if (!process_num(ABCVARAS, sgk, sgb, n, incr, 0, &res))
        {
            OutputBoth("VerifyCert failed.\n");
            return FALSE;
        }
        ReadUInt64(res64, &testRes64);
        if (testRes64 != testCert64)
        {
            OutputBoth("Certificate mismatch.\n");
            return FALSE;
        }

        PROOFMODE = FullTest;
    }

    return TRUE;
}

int DoProthTest(struct KNTest *knTest)
{
    static char sk[sgkbufsize];
    static char sb[2] = "2";

    sprintf(sk, "%u", knTest->k);
    return DoTest(sk, sb, knTest->n, 1, knTest->res64, knTest->cert64);
}

int DoLLRTest(struct KNTest *knTest)
{
    static char sk[sgkbufsize];
    static char sb[2] = "2";

    sprintf(sk, "%u", knTest->k);
    return DoTest(sk, sb, knTest->n, -1, knTest->res64, knTest->cert64);
}

int DoPLMTest(struct KBNTest *kbnTest, int c)
{
    static char sk[sgkbufsize];
    static char sb[sgkbufsize];

    sprintf(sk, "%u", kbnTest->k);
    sprintf(sb, "%u", kbnTest->b);
    return DoTest(sk, sb, kbnTest->n, c, kbnTest->res64, kbnTest->cert64);
}

int DoAnyTest(struct KBNCTest *kbncTest)
{
    return DoTest(kbncTest->sk, kbncTest->sb, kbncTest->n, kbncTest->c, kbncTest->res64, kbncTest->cert64);
}

int DoTestSubset(char *subset)
{
    struct KNTest *knTest;
    struct KBNTest *kbnTest;
    struct KBNCTest *kbncTest;

    if (!strcmp(subset, "all") || !strcmp(subset, "321plus"))
    {
        OutputBoth("Running 321plus tests.\n");
        for (knTest = Test321Plus; knTest->n != 0; knTest++)
            if (!DoProthTest(knTest))
                return FALSE;
    }

    if (!strcmp(subset, "all") || !strcmp(subset, "321minus"))
    {
        OutputBoth("Running 321minus tests.\n");
        for (knTest = Test321Minus; knTest->n != 0; knTest++)
            if (!DoLLRTest(knTest))
                return FALSE;
    }

    if (!strcmp(subset, "all") || !strcmp(subset, "b5plus"))
    {
        OutputBoth("Running b5plus tests.\n");
        for (kbnTest = TestBase5Plus; kbnTest->n != 0; kbnTest++)
            if (!DoPLMTest(kbnTest, 1))
                return FALSE;
    }

    if (!strcmp(subset, "all") || !strcmp(subset, "b5minus"))
    {
        OutputBoth("Running b5minus tests.\n");
        for (kbnTest = TestBase5Minus; kbnTest->n != 0; kbnTest++)
            if (!DoPLMTest(kbnTest, -1))
                return FALSE;
    }

    if (!strcmp(subset, "all") || !strcmp(subset, "special"))
    {
        OutputBoth("Running special tests.\n");
        for (kbncTest = TestSpecial; kbncTest->n != 0; kbncTest++)
            if (!DoAnyTest(kbncTest))
                return FALSE;
    }

    if (!strcmp(subset, "all") || !strcmp(subset, "prime"))
    {
        OutputBoth("Running prime tests.\n");
        for (kbncTest = TestPrime; kbncTest->n != 0; kbncTest++)
            if (!DoAnyTest(kbncTest))
                return FALSE;
    }

    return TRUE;
}

char TestSubset[IBSIZE];

void DoTests()
{
    primolimit = 0;
    if (PROOFMODE == FullTest)
    {
        IniWriteInt(INI_FILE, "Gerbicz", 1);
        IniWriteInt(INI_FILE, "ProofCount", 16);
        IniWriteInt(INI_FILE, "DeletePoints", 1);
        IniWriteString(INI_FILE, "RndSeed", "123456");
        readIniFiles();
    }
    test_fft_inc = IniGetInt(INI_FILE, "FFT_Increment", 0);

    if (DoTestSubset(TestSubset))
        OutputBoth("All tests completed successfully.\n");

    IniWriteInt(INI_FILE, "FFT_Increment", test_fft_inc);
}